/**
 * Reminder-тексты для habit-tracker'а: нормал-обёртки трёх типов reminder'ов
 * (утренний, вечерний, per-habit) + replacing/overlay-триггеры.
 *
 * Согласовано с Эмином 2026-05-12/13. Источник истины: `docs/reminder-texts.md`.
 *
 * **Структура каждого типа reminder'а:**
 * - Утренний: header + список привычек + footer (header и footer варьируются).
 * - Вечерний: header (фикс «🌙 Время подвести итоги дня!») + intro (варьируется) +
 *   checklist с inline-кнопками. Footer'а нет.
 * - Per-habit: единый шаблон «⏰ {текст}: *{эмодзи} {название}*» с одной кнопкой.
 *
 * **Триггеры:**
 * - `replacing` — заменяют normal обёртку (missed_*, all_completed).
 * - `overlay` — добавляются строкой к normal или поверх replacing
 *   (near_milestone, perfect_week_ahead, freeze_used).
 *
 * Плейсхолдеры:
 * - `{name}` — `habit.name` без кавычек (для per-habit triggers; в шаблоне сами
 *   рисуем «»).
 * - `{emoji}` — `habit.emoji`.
 * - `{n}` — milestone-число в днях (для near_milestone).
 * - `{suffix}` — динамический суффикс (для freeze_used / freeze_earned).
 *
 * @module data/reminderTexts
 */

export type TextPiece = {
  id: string;
  text: string;
};

export type HeaderFooterPair = {
  id: string;
  header: string;
  footer: string;
};

