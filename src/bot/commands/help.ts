import { BotContext } from '../../types/index.js';
import { createMainMenuKeyboard } from '../keyboards/index.js';

/**
 * Обработчик команды /help
 * @param ctx - Контекст бота
 */
export const handleHelp = async (ctx: BotContext): Promise<void> => {
  const helpMessage = `
📖 *Справка по боту*

*Команды:*
/start — Главное меню
/help — Эта справка
/habits — Список привычек
/stats — Статистика
/settings — Настройки
/feedback — Обратная связь

*Как пользоваться:*

🔹 *Добавление привычки*
Нажми "➕ Добавить" и следуй инструкциям

🔹 *Отметка выполнения*
Нажми на привычку в списке, чтобы отметить её выполненной ✅ или отменить отметку ⬜

🔹 *Удаление привычки*
Нажми 🗑 рядом с привычкой

🔹 *Напоминания*
Бот присылает напоминания:
• 🌅 Утром — список привычек на день
• 🌙 Вечером — чтобы отметить выполненные

Время можно настроить в ⚙️ Настройках

🔹 *Привычки раз в N дней*
Можно создать привычку, которую нужно выполнять раз в 2, 3 или 7 дней. Такие привычки будут показываться только в нужные дни.

🔹 *Статистика*
Смотри streak'и 🔥, процент выполнения и прогресс по каждой привычке

🔹 *Обратная связь*
/feedback — расскажи что не работает или чего не хватает. На важные сообщения автор отвечает прямо в чат
  `.trim();

  await ctx.reply(helpMessage, {
    parse_mode: 'Markdown',
    reply_markup: createMainMenuKeyboard(),
  });
};
